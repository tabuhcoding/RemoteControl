    private void viewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewActionPerformed
        connection();
        String signal = "VIEWAPP";
        PrintWriter pr = null;
        try {
            pr = new PrintWriter(Program.socket.getOutputStream());
        } catch (IOException ex) {
            ex.printStackTrace();
        }
        pr.println(signal);
        pr.flush();
        BufferedReader bf = null;
        try {
            bf = new BufferedReader(new InputStreamReader(Program.socket.getInputStream()));
        } catch (IOException ex) {
            Logger.getLogger(listAppDesign.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            String str = bf.readLine();
            while(str != null){
                String space = "";
                for(int i = 0; i < str.length() - 1; ++i){
                    if(str.charAt(i) == ' '){
                        space += " ";
                    }
                }
                String[] s = str.split(space);
                int i = 1;
                String col1 = "", col2 = "";
                for(String w:s){
                    if(i == 1){
                        col1 = w;
                    }else {
                        col2 = w;
                    }
                    i++;
                }
                model = (DefaultTableModel) jTable1.getModel();
                model.addRow(new Object[] {
                    col1, col2
                });
                str = bf.readLine();
            }
        } catch (IOException ex) {
            Logger.getLogger(listAppDesign.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_viewActionPerformed

